package stringExercises;

public class Main {

	public static void main(String[] args) {
		
		// String은 굉장히 특이한 class
		// String은 기본 data type이 아니다.
		String a = new String("안녕하세요!");
		String c = new String("안녕하세요!");
		
		// JAVA에서 문자열은 instance이다. (String 자체가 class)
		// 그래서 문자열을 만드려면, 위와 같이 instance를 만들어야 한다.
		
		String b = "소리없는 아우성!";
		String d = "소리없는 아우성!";		
		
		
	}
}
